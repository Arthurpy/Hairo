<template>
  <div class="bg-blue-200 flex">
    <sidebar :activeButton="'ressources'"/>
    <div class="flex flex-col ml-80">
      <div class="flex bg-white text-[#2176FF] w-[70vw] h-[56px] ml-[50px] rounded-lg justify-between items-center mt-10 py-16">
        <h1 class="search-title text-[#2176FF] text-5xl font-bold flex items-center ml-10">Ressources</h1>
        <img src="../assets/book.png" alt="search" class="w-auto" />
      </div>
      <div class="bg-white text-[#2176FF] w-[70vw] h-[91px] rounded-lg mt-[30px] ml-[50px] flex items-center text-base p-[15px]">
        <h1 class="text-[#2176FF] text-2xl font-semibold">Que rechercher vous?</h1>
        <input type="text" v-model="searchQuery" class="input-rounded input ml-56" placeholder="Rechercher un thÃ¨me">
      </div>
      <div v-for="(row, rowIndex) in filteredChunkedCourses" :key="rowIndex" class="flex flex-row ml-[50px] mt-[10px] ">
        <router-link v-for="(course, index) in row" :key="index" :to="{ name: 'CourseDetails', params: { courseName: course } }" class="bg-[#2176FF] w-[21.5%] rounded-lg m-2">
          <img src="../assets/Anat.jpeg" class=" rounded-t-lg"/>
          <h1 class="text-white font-semibold text-2xl mx-2 p-2 items-center"> {{ course }} </h1>
        </router-link>
      </div>
    </div>
  </div>
</template>

<script>
import sidebar from './../components/sidebar.vue';
import pacesFolders from './../assets/pacesFolders.json';

export default {
  name: 'ressources',
  components: {
    sidebar,
  },
  data() {
    return {
      isLightTheme: true,
      courses: pacesFolders,
      searchQuery: '',
    };
  },
  computed: {
    chunkedCourses() {
      const chunkSize = 4;
      const chunkedArray = [];
      for (let i = 0; i < this.courses.length; i += chunkSize) {
        chunkedArray.push(this.courses.slice(i, i + chunkSize));
      }
      return chunkedArray;
    },
    filteredCourses() {
      const query = this.searchQuery.toLowerCase();
      return this.courses.filter(course => course.toLowerCase().includes(query));
    },
    filteredChunkedCourses() {
      const chunkSize = 4;
      const chunkedArray = [];
      for (let i = 0; i < this.filteredCourses.length; i += chunkSize) {
        chunkedArray.push(this.filteredCourses.slice(i, i + chunkSize));
      }
      return chunkedArray;
    },
  },
};
</script>